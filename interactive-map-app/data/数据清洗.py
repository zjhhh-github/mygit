# 常见冗余字符和替换规则
CLEAN_RULES = {
    r'\s+': '',          # 移除所有空格
    r'[,，。.、；;：:]': '', # 移除常见标点
    r'（.*?）': '',      # 移除括号及括号内内容
    r'\(.*?\)': '',      # 移除英文括号及内容
    r'号.*': '',         # 移除门牌号后的冗余信息（可根据需求调整）
}
MUNICIPALITIES = ['北京市', '上海市', '天津市', '重庆市','北京','上海','天津','重庆']
# 2. 升级后的地址清洗核心函数（支持无省份补全）
def clean_address(raw_address):
    """
    清洗原始地址，返回标准化地址（支持无省份时自动补全）
    :param raw_address: 原始非标准地址字符串
    :return: 标准化后的地址字典（包含省、市、区、详细地址）
    """
    if not raw_address or not isinstance(raw_address, str):
        return {'province': '', 'city': '', 'district': '', 'detail': '', 'full_standard_address': ''}
    
    # 步骤1：基础清洗（移除冗余字符、空格）
    clean_addr = raw_address.strip()
    for pattern, replace in CLEAN_RULES.items():
        clean_addr = re.sub(pattern, replace, clean_addr)
    
    # 初始化变量
    province = ''
    city = ''
    district = ''
    detail = clean_addr
    
    # 步骤2：优先识别省份（处理有省份的情况）
    for short, full in PROVINCE_MAP.items():
        if clean_addr.startswith(full):
            province = full
            detail = clean_addr[len(full):]
            break
        elif clean_addr.startswith(short) and len(short) == 1:
            province = full
            detail = clean_addr[len(short):]
            break
    
    # 步骤3：识别城市（优先匹配完整城市名，解决无省份的核心步骤）
    # 先按完整城市名匹配（精准）
    matched_city = ''
    for city_name in CITY_TO_PROVINCE.keys():
        if city_name in detail:
            matched_city = city_name
            break
    # 若未匹配到完整城市名，再用后缀匹配（兼容简写）
    if not matched_city:
        city_pattern = r'([^省市区县]+[市州盟])'
        city_match = re.search(city_pattern, detail)
        if city_match:
            matched_city = city_match.group(1)
    
    # 提取城市并补全省份
    if matched_city:
        city = matched_city
        # 核心：如果没有识别到省份，通过城市反向查省份
        if not province and city in CITY_TO_PROVINCE:
            province = CITY_TO_PROVINCE[city]
        # 从详细地址中移除城市名
        detail = detail.replace(city, '')
    
    # 步骤4：识别区/县
    district_pattern = r'([^省市]+[区县旗])'
    district_match = re.search(district_pattern, detail)
    if district_match:
        district = district_match.group(1)
        detail = detail.replace(district, '')
    
    # 步骤5：最终清洗详细地址
    detail = detail.strip()
    
    # 组装完整标准化地址
    full_address = f'{province}{city}{district}{detail}'
    # 核心：如果没有识别到省份，且城市在直辖市列表中，默认省份为城市名
    if not city and province in MUNICIPALITIES:
        city = province
    
    return {
        'province': province,
        'city': city,
        'district': district,
        'detail': detail,
        'full_standard_address': full_address
    }

# 从JSON文件加载地址映射数据
def load_address_mapping_from_json(json_file_path):
    """
    从JSON文件加载地址映射数据
    :param json_file_path: JSON文件路径
    :return: 包含省份、城市、区域映射的字典
    """
    with open(json_file_path, 'r', encoding='utf-8') as f:
        mapping_data = json.load(f)
    
    # 从JSON数据构建映射表
    province_map = PROVINCE_MAP.copy()
    city_to_province = CITY_TO_PROVINCE.copy()  # 基于现有的映射表进行扩展
    
    # 将JSON中的数据转换为映射表格式
    for province, cities in mapping_data.items():
        # 添加城市到省份的映射
        for city in cities.keys():
            city_to_province[city] = province
            # 同时添加省份名称作为自己的映射（适用于直辖市等情况）
            if city == province:
                city_to_province[city] = province
    
    return mapping_data, province_map, city_to_province

# 更新全局映射表
def update_global_mappings(json_file_path):
    """
    使用JSON文件中的数据更新全局映射表
    """
    global PROVINCE_MAP, CITY_TO_PROVINCE
    mapping_data, new_province_map, new_city_to_province = load_address_mapping_from_json(json_file_path)
    
    # 更新映射表，保留原有的映射关系
    PROVINCE_MAP.update(new_province_map)
    CITY_TO_PROVINCE.update(new_city_to_province)

# 清洗CSV文件中的地址
def process_csv_addresses(csv_file_path, output_file_path=None):
    """
    处理CSV文件中的地址数据
    :param csv_file_path: 输入CSV文件路径
    :param output_file_path: 输出CSV文件路径（可选）
    :return: 处理后的DataFrame
    """
    # 读取CSV文件
    df = pd.read_csv(csv_file_path, encoding='gbk',header=1)
    
    # 创建存储清洗结果的列
    df['province'] = ''
    df['city'] = ''
    df['district'] = ''
    df['detail'] = ''
    df['full_standard_address'] = ''
    
    # 对每一行地址进行清洗
    for idx, row in df.iterrows():
        addr = row.iloc[1] if len(row) > 1 else ''  # 假设地址在第二列
        if isinstance(addr, str):
            result = clean_address(addr)
            df.at[idx, 'province'] = result['province']
            df.at[idx, 'city'] = result['city']
            df.at[idx, 'district'] = result['district']
            df.at[idx, 'detail'] = result['detail']
            df.at[idx, 'full_standard_address'] = result['full_standard_address']
    print(df)
    # 如果提供了输出路径，则保存结果
    if output_file_path:
        df.to_csv(output_file_path, index=False, encoding='utf-8')
        print(f"清洗结果已保存到: {output_file_path}")
    
    return df

